def get_connection():
    conn = sqlite3.connect(DB_PATH)
    conn.row_factory = sqlite3.Row
    return conn


def init_db():
    conn = get_connection()
    cursor = conn.cursor()
    cursor.execute("""
        CREATE TABLE IF NOT EXISTS questions (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            category TEXT NOT NULL,
            question_number INTEGER NOT NULL,
            text TEXT NOT NULL,
            expectation TEXT
        )
    """)
    conn.commit()
    conn.close()
    populate_if_empty()


