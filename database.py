def get_all_questions():
    conn = get_connection()
    cursor = conn.cursor()
    cursor.execute("SELECT * FROM questions ORDER BY id")
    questions = [dict(row) for row in cursor.fetchall()]
    conn.close()
    return questions


def get_question_by_id(question_id):
    conn = get_connection()
    cursor = conn.cursor()
    cursor.execute("SELECT * FROM questions WHERE id = ?", (question_id,))
    question = cursor.fetchone()
    conn.close()
    if question:
        return dict(question)
    return None


def get_question_by_number(question_number):
    conn = get_connection()
    cursor = conn.cursor()
    cursor.execute("SELECT * FROM questions WHERE question_number = ?", (question_number,))
    question = cursor.fetchone()
    conn.close()
    if question:
        return dict(question)
    return None


def get_questions_by_category(category):
    conn = get_connection()
    cursor = conn.cursor()
    cursor.execute("SELECT * FROM questions WHERE category = ? ORDER BY question_number", (category,))
    questions = [dict(row) for row in cursor.fetchall()]
    conn.close()
    return questions


def get_categories():
    conn = get_connection()
    cursor = conn.cursor()
    cursor.execute("SELECT DISTINCT category FROM questions ORDER BY category")
    categories = [row["category"] for row in cursor.fetchall()]
    conn.close()
    return categories
